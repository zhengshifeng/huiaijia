public class WmsGoodsInfo {

	private String goodsNo; // ECLP事业部商品编码（修改时用，作为主键）
	private String deptNo; // 事业部编码（货主）
	private String isvGoodsNo; // ISV主商品编码
	private String spGoodsNo; // 销售平台商品编码 
	private String barcodes; // 商品条码，以英文逗号隔开 
	private String thirdCategoryNo; // 商品三级分类编码 
	private String goodsName; // 商品名称 
	private String abbreviation; // 商品简称 
	private String brandNo; // 品牌编码 
	private String brandName; // 品牌名称 
	private String manufacturer; // 生产厂商 
	private String produceAddress; // 产地 
	private String standard; // 商品规格 

	public String getGoodsNo() {
		return goodsNo;
	}

	public void setGoodsNo(String goodsNo) {
		this.goodsNo = goodsNo;
	}

	public String getDeptNo() {
		return deptNo;
	}

	public void setDeptNo(String deptNo) {
		this.deptNo = deptNo;
	}

	public String getIsvGoodsNo() {
		return isvGoodsNo;
	}

	public void setIsvGoodsNo(String isvGoodsNo) {
		this.isvGoodsNo = isvGoodsNo;
	}

	public String getSpGoodsNo() {
		return spGoodsNo;
	}

	public void setSpGoodsNo(String spGoodsNo) {
		this.spGoodsNo = spGoodsNo;
	}

	public String getBarcodes() {
		return barcodes;
	}

	public void setBarcodes(String barcodes) {
		this.barcodes = barcodes;
	}

	public String getThirdCategoryNo() {
		return thirdCategoryNo;
	}

	public void setThirdCategoryNo(String thirdCategoryNo) {
		this.thirdCategoryNo = thirdCategoryNo;
	}

	public String getGoodsName() {
		return goodsName;
	}

	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}

	public String getAbbreviation() {
		return abbreviation;
	}

	public void setAbbreviation(String abbreviation) {
		this.abbreviation = abbreviation;
	}

	public String getBrandNo() {
		return brandNo;
	}

	public void setBrandNo(String brandNo) {
		this.brandNo = brandNo;
	}

	public String getBrandName() {
		return brandName;
	}

	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}

	public String getManufacturer() {
		return manufacturer;
	}

	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}

	public String getProduceAddress() {
		return produceAddress;
	}

	public void setProduceAddress(String produceAddress) {
		this.produceAddress = produceAddress;
	}

	public String getStandard() {
		return standard;
	}

	public void setStandard(String standard) {
		this.standard = standard;
	}
}
