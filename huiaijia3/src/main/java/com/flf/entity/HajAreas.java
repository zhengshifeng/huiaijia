package com.flf.entity;

import com.base.entity.BaseEntity;

/**
 * 
 * <br>
 * <b>功能：</b>HajAreasEntity<br>
 */
public class HajAreas extends BaseEntity {
	private static final long serialVersionUID = 6778750033861502889L;
	private java.lang.Integer id;// 主键（建索引）
	private java.lang.String code;// 编号
	private java.lang.String name;// 名称
	private java.lang.String pCode;// 父编号
	private java.lang.Integer level;// 级别

	public java.lang.Integer getId() {
		return this.id;
	}

	public void setId(java.lang.Integer id) {
		this.id = id;
	}

	public java.lang.String getCode() {
		return this.code;
	}

	public void setCode(java.lang.String code) {
		this.code = code;
	}

	public java.lang.String getName() {
		return this.name;
	}

	public void setName(java.lang.String name) {
		this.name = name;
	}

	public java.lang.String getPCode() {
		return this.pCode;
	}

	public void setPCode(java.lang.String pCode) {
		this.pCode = pCode;
	}

	public java.lang.Integer getLevel() {
		return this.level;
	}

	public void setLevel(java.lang.Integer level) {
		this.level = level;
	}

	@Override
	public String toString() {
		return "HajAreas{" +
				"id=" + id +
				", code='" + code + '\'' +
				", name='" + name + '\'' +
				", pCode='" + pCode + '\'' +
				", level=" + level +
				'}';
	}
}
