package com.flf.entity;

import com.base.entity.BaseEntity;

/**
 * 用于小区分拣小组管理视图层查询条件
 *
 * Created by SevenWong on 2016/12/29 17:55.
 */
public class HajCommunitySorterVo extends BaseEntity{

	private Integer id;
	private String provinceCode; // 省份编号
	private String cityCode; // 城市编号
	private String communityCode; // 行政区编号
	private String orderByClause; // 排序条件编号
	private Integer sorterId; // 分拣小区ID
	private String communityName; // 小区名或ID

	private Page page;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getProvinceCode() {
		return provinceCode;
	}

	public void setProvinceCode(String provinceCode) {
		this.provinceCode = provinceCode;
	}

	public String getCityCode() {
		return cityCode;
	}

	public void setCityCode(String cityCode) {
		this.cityCode = cityCode;
	}

	public String getCommunityCode() {
		return communityCode;
	}

	public void setCommunityCode(String communityCode) {
		this.communityCode = communityCode;
	}

	public String getOrderByClause() {
		return orderByClause;
	}

	public void setOrderByClause(String orderByClause) {
		this.orderByClause = orderByClause;
	}

	public Integer getSorterId() {
		return sorterId;
	}

	public void setSorterId(Integer sorterId) {
		this.sorterId = sorterId;
	}

	public String getCommunityName() {
		return communityName;
	}

	public void setCommunityName(String communityName) {
		this.communityName = communityName;
	}

	public Page getPage() {
		return page;
	}

	public void setPage(Page page) {
		this.page = page;
	}
}
